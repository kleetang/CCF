                    <p>CCF 亦有小組聚會，分為國語及廣東兩個小組，在每一個星期三的晚上舉行。可以給大家更多的機會互相分享及關懷各人的屬靈生活。 可按自己意願選擇適合的小組來參加。小組是以詩歌、遊戲、查經及個人分享讓大家對聖經，自己，即彼此有更深的認識。</p>
