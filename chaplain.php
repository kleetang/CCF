                  My Prayer
                  “ No Reserves, No Retreats, No Regrets.”
                  不保留，不退縮，不後悔

                    <p>「林姐」是CCF稱林恂牧師特有的專利「暱名」；因當時的林姐只比研究生長幾歲；她於’90年接續了CCF的傳道，招世超牧師之職；’92年一月，經過了十次裡裡外外的面談，她進入BCM校牧室，成為Director of BCM,也是Stony Brook的校牧成員；從此CCF也隨著林姐，進入BCM的陽傘下；尤其是’90年代後期，校方因人數增加對社團限制非常多，然CCF因著與BCM的銜接，能夠不受影響的迅速成長，成為一個屬靈的大家庭；週五晚，聚會滿到窗台座；GC我們以150人為底線；林姐工作表是七天的；也把一生最寶貴的青春奉獻給石溪的校園事工。她說：那是我無怨無悔的獻上，也是我事奉主的生命中至深的祝福！ </p>
                    <p>2003年，林姐以為該自學生事工中退休了，在經過兩年的等候與禱告後，遂赴NC，開始發展推廣神學教育的跨教會宣道生活；沒有任何差會或教會或神學院的支持，只有幾位校友，憑信心地走過了另一decade. 至於CCF呢？2006年，BCM更換人事，改換宗旨；林姐回來與校方商談，之後成立亞裔校牧室(ACCM)；CCF此後換了把陽傘；林姐人仍舊作那打傘服事CCF的幕後支持者；她說：校園事工原來是我一生的呼召…</p>
