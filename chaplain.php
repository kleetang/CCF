                    <p>Chinese Christian Fellowship 簡稱 CCF 的中文名稱是「石溪大學團契」。 我們建立於八十年代初期。CCF 聚集校內許多的華人學生，幫助他們認識及了解基督教、聖經及基督徒的生活。 我們的社團主要成員大部份來自香港、中國大陸及台灣。</p>
                    <p>1991年開始CCF由Baptist Campus Ministry贊助，2010年開始CCF由Asian Christian Campus Ministry贊助。在Stony Brook University Union，CCF有兩間辦公室。</p>
